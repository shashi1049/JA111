package com.shashi;

public class Array2 {
	
	public static void main(String[] args) {
		// create an array
		int[] marks = {58, 45, 52};
		// loop through the array
		// using for loop
		System.out.println("Using for Loop:");
		for(int i = 0; i < marks.length; i++) {
		System.out.println(marks[i]);
		}
		}

}
